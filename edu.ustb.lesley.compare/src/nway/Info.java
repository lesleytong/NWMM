package nway;

public class Info {

	int size;
	int sumMinCost;
	
	public Info(int size, int minCost) {
		this.size = size;
		this.sumMinCost = minCost;
	}
	
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public int getMinCost() {
		return sumMinCost;
	}
	public void setMinCost(int minCost) {
		this.sumMinCost = minCost;
	}
	
	
	
}
